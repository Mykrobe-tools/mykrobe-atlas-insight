CREATE TABLE results (
    id INT(6) UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    type VARCHAR(30) NOT NULL,
    analysed VARCHAR(100),
    kmer BIGINT,
    genotypeModel VARCHAR(100),
    susceptibility_isoniazid VARCHAR(100),
    susceptibility_kanamycin VARCHAR(100),
    susceptibility_ethambutol VARCHAR(100),
    susceptibility_streptomycin VARCHAR(100),
    susceptibility_capreomycin VARCHAR(100),
    susceptibility_quinolones VARCHAR(100),
    susceptibility_pyrazinamide VARCHAR(100),
    susceptibility_rifampicin VARCHAR(100),
    susceptibility_amikacin VARCHAR(100),
    phylogenetics_species_result VARCHAR(100),
    phylogenetics_species_percent_coverage FLOAT,
    phylogenetics_species_median_depth BIGINT,
    phylogenetics_phylo_group_result VARCHAR(100),
    phylogenetics_phylo_group_percent_coverage FLOAT,
    phylogenetics_phylo_group_median_depth BIGINT,
    phylogenetics_sub_complex_result VARCHAR(100),
    phylogenetics_sub_complex_percent_coverage FLOAT,
    phylogenetics_sub_complex_median_depth BIGINT,
    phylogenetics_lineage_result VARCHAR(100),
    phylogenetics_lineage_percent_coverage FLOAT,
    phylogenetics_lineage_median_depth BIGINT,
    parent_id VARCHAR(30) NOT NULL,
    received_date TIMESTAMP
);